#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	i;
	int	c;

	i = 0;
	c = 1;
	while (str[i] && c == 1)
	{
		if ((str[i] >= 'A' && str[i] <= 'Z')
			|| (str[i] >= 'a' && str[i] <= 'z'))
		{
			c = 1;
		}
		else
		{
			c = 0;
		}
		i++;
	}
	return (c);
}
